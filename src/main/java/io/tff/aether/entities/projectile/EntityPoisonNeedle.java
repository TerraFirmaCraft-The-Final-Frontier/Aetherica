package io.tff.aether.entities.projectile;

import io.tff.aether.entities.projectile.darts.EntityDartPoison;
import net.minecraft.entity.EntityLiving;
import net.minecraft.world.World;

public class EntityPoisonNeedle extends EntityDartPoison
{

    public EntityPoisonNeedle(World world)
    {
        super(world);
    }

    public EntityPoisonNeedle(World world, EntityLiving ent)
    {
        super(world, ent);
    }

    public void entityInit()
    {
        super.entityInit();
        this.setDamage(1);
    }

    @Override
    public boolean hasNoGravity()
    {
        return false;
    }

}